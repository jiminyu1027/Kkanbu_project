package edu.study.vo;

public class QaVO {
	
	private int pqidx;
	private String pqSubject;
	private String pqContents;
	private String pqWriter;
	private String pqOpen;
	private int midx;
	private String pqWriteday;
	public int getPqidx() {
		return pqidx;
	}
	public void setPqidx(int pqidx) {
		this.pqidx = pqidx;
	}
	public String getPqSubject() {
		return pqSubject;
	}
	public void setPqSubject(String pqSubject) {
		this.pqSubject = pqSubject;
	}
	public String getPqContents() {
		return pqContents;
	}
	public void setPqContents(String pqContents) {
		this.pqContents = pqContents;
	}
	public String getPqWriter() {
		return pqWriter;
	}
	public void setPqWriter(String pqWriter) {
		this.pqWriter = pqWriter;
	}
	public String getPqOpen() {
		return pqOpen;
	}
	public void setPqOpen(String pqOpen) {
		this.pqOpen = pqOpen;
	}
	public int getMidx() {
		return midx;
	}
	public void setMidx(int midx) {
		this.midx = midx;
	}
	public String getPqWriteday() {
		return pqWriteday;
	}
	public void setPqWriteday(String pqWriteday) {
		this.pqWriteday = pqWriteday;
	}
}




